    /**
     * Valid cc: 14 - 19 digits in length, passes luhn test
     *
     * @param ccNum
     * @return true or false based on validity of passed in credit card number
     */

    public static boolean validateCreditCard(long ccNum){

        String ccNumString = Long.toString(ccNum);
        if (ccNumString.length() < 14 || ccNumString.length() > 19) return false;

        int iteration = 1;
        int sum = 0;
        for (int i = ccNumString.length() - 1; i > -1;i--){
            int addable = Character.getNumericValue(ccNumString.charAt(i));
            if (!Character.isDigit(ccNumString.charAt(i))){
                return false;
            } else if (iteration % 2 == 0){
                addable *= 2;
                if (addable > 9){
                    addable -= 9;

                }
            }
            iteration++;
            sum += addable;
        }

        return sum % 10 == 0;
    }
